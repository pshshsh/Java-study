package week02;

public class w03 {
  //비교연산자 예제
  public static void main(String[] args) {
    System.out.println(10>9); // 10이 9보다 크니?
    System.out.println(10>=9);
    System.out.println(10 < 9);
    System.out.println(10<=9);
    System.out.println(10 == 10);
    System.out.println(10 == 9);
    System.out.println(10 != 10);
    System.out.println(10 != 9);
  }
}
